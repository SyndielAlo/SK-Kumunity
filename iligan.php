  <h1>Government</h1>

<img src="images/logo iligan.png" alt="iligan">

<article>
  
Iligan is a highly urbanized city and is politically independent of the province of Lanao del Norte. Registered voters of the city no longer vote for provincial candidates such as the Governor and Vice Governor, unlike its nearby towns that make up the provinces as a result of its charter as a city in the 1950s.[citation needed]

Iligan's seat of government, the city hall, is located at Buhanginan Hills in Barangay Pala-o. The local government structure is composed of one mayor, one vice mayor, and twelve councilors. Each official is elected publicly to a 3-year term and can be re-elected up to 3 terms in succession. The day-to-day administration of the city is handled by the city administrator.
