Iligan, officially the City of Iligan (Cebuano: Dakbayan sa Iligan; Filipino: Lungsod ng Iligan; Maranao: Inged a Iligan), is a 1st class highly urbanized city in the region of Northern Mindanao, Philippines. According to the 2020 census, it has a population of 363,115 people.

It is geographically within the province of Lanao del Norte but administered independently from the province. It was once part of Central Mindanao (Region 12) until the province was moved under Northern Mindanao (Region 10) in 2001.[5] Iligan is approximately 90 kilometers away from the capital of the province, Tubod, and approximately 800 kilometers from the capital of the Philippines, Manila.

Iligan has a total land area of 813.37 square kilometres (314.04 sq mi), making it one of the 10 largest cities in the Philippines in terms of land area. Among the 33 highly urbanized cities of the Philippines, Iligan is the third-least dense, with a population density of 421 inhabitants per square kilometer, just behind Butuan and Puerto Princesa.



